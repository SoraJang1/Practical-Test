package day06;

public class ComplexOp {
	
	public static void main(String[] args) {
		
		int x=10;
		x=x+10; // x=20
		
		int y=10;
		y+=10;
		
		System.out.println(x);
		System.out.println(y);
		
		int a=3;
		a=a+3;
		
		int b=3;
		b+=3;
		
		System.out.println(a);
		System.out.println(b);
		
		
	}

}
